@app.middleware("http")
async def request_context_middleware(request: Request, call_next):
    """Attach request id, security headers, and HTTP-level metrics for each request."""

    request_id = request.headers.get("X-Request-ID", str(uuid4()))
    request.state.request_id = request_id
    start = perf_counter()
    response: Response | None = None

    try:
        if request.method in {"POST", "PUT", "PATCH"}:
            raw_content_length = request.headers.get("content-length", "").strip()
            if raw_content_length:
                try:
                    content_length = int(raw_content_length)
                except ValueError:
                    response = JSONResponse(
                        status_code=400,
                        content=_build_error_payload(
                            request=request,
                            code="bad_request",
                            message="invalid_content_length_header",
                        ),
                    )
                else:
                    if content_length > settings.max_payload_bytes:
                        response = JSONResponse(
                            status_code=413,
                            content=_build_error_payload(
                                request=request,
                                code="payload_too_large",
                                message="payload_too_large",
                            ),
                        )

        if response is None:
            response = await call_next(request)
    except Exception:
        REQUEST_COUNTER.labels(
            method=request.method,
            endpoint=request.url.path,
            status="500",
        ).inc()
        raise
    finally:
        elapsed = perf_counter() - start
        REQUEST_LATENCY.labels(method=request.method, endpoint=request.url.path).observe(elapsed)

    REQUEST_COUNTER.labels(method=request.method, endpoint=request.url.path, status=str(response.status_code)).inc()

    response.headers["X-Request-ID"] = request_id
    _apply_standard_security_headers(response)
    return response


